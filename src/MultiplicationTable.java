//Programme to display multiplication

public class MultiplicationTable
{
    public static void main(String[] args)
    {

        int a = 1;

        do
        {
            for (int i = 1; i <= 1; i++)
            {
                System.out.print(a * 1 + " " + a * 2 + " " + a * 3);
            }
            System.out.println();
            a++;
        }while(a<4);

    }
}
